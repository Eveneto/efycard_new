				const filterItems = document.querySelectorAll(".portfolio-filter-menu li");
				
				// Obtém todos os itens de serviço
				const serviceItems = document.querySelectorAll(".service-item");

				// Adiciona evento de clique nos filtros
				filterItems.forEach(item => {
					item.addEventListener("click", function() {
						// Remove a classe 'active' de todos os filtros
						filterItems.forEach(filter => filter.classList.remove("active"));
						// Adiciona a classe 'active' ao filtro clicado
						item.classList.add("active");

						// Obtém o filtro selecionado
						const filterValue = item.getAttribute("data-filter");

						// Mostra ou oculta os itens com base no filtro
						serviceItems.forEach(service => {
							if (filterValue === "*" || service.classList.contains(filterValue)) {
								service.style.display = "block"; // Exibe o item
							} else {
								service.style.display = "none"; // Oculta o item
							}
						});
					});
				});
			});
		</script>

